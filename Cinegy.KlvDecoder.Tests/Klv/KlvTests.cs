        [Test]
        public void DecodeKlvData()
        {
            var entities = KlvEntityFactory.GetEntitiesFromData(KlvDataSample1);

            foreach (var entity in entities)
            {
                Console.WriteLine($"KEY:{entity.KeyHexString}");
                Console.WriteLine($"VAL ({entity.Value.Length}): {entity.ValueHexString}");
            }

            if (entities.Count != 1)
                Assert.Fail("Did not find a single KLV in returned data");

            if (!entities[0].KeyHexString.Equals("06-0E-2B-34-02-05-01-01-0D-01-02-01-01-02-04-00"))
                Assert.Fail("Did not find SMPTE MXF Closed Complete Header Partition Pack key as KLV value");

            if (entities[0].Value.Length != 120)
                Assert.Fail("Returned value data length was not the expected 120 bytes known to be in the sample");
        }
        
    }
